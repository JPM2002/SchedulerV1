def get_time_left_message():
    now = datetime.now()
    end = now.replace(hour=23, minute=59, second=59, microsecond=0)
    delta = end - now
    hours, remainder = divmod(delta.seconds, 3600)
    minutes = remainder // 60
    return f"ðŸ•’ Only {hours}h {minutes}m left until midnight!"
